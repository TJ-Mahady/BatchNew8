package com.syntax.class08;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		for (int i=5; i<10; i++) {
			System.out.print("Good Morning"+" ");
		}

		for(int i=1; i<100; i++) {
			
			System.out.println(i);
		}
		
		for(int i=10; i>=0; i--) {
			System.out.print(i+" ");
		}
		
		for (int i=0; i<=30; i+=3) {
			System.out.println(i);
		}
	}

}
