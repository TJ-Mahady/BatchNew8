package com.syntax.class01;

public class DataTypesAndVariables {

	public static void main(String[] args) {

		//primitive data types
		//numeric
		
		byte variable1=4; //-128 to 127

		short variable2=100; //-32768 to 32767
		
		int variable3=1000; // will be using the most
		
		long variable4=1000000890734567L;
		
		// to store floating numbers
		
		float var1=12.99f;
		double var2=9.99999;
		
		//represent yes or no (true or false)
		
		boolean myVariable=true;
		boolean yourVariable=false;

		//to represent single character
		
		char myBox='C';
		char myBox2='&';
		char myBox3='7';
		
		System.out.println();

	}

}
